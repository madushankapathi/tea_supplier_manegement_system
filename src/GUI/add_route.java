    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        String id = jTextField2.getText();
        ResultSet rs1 = DBMS.search("SELECT * FROM route WHERE `id`='"+id+"' ");
        TableModel tm = jTable1.getModel();
        DefaultTableModel dtm = (DefaultTableModel) tm;
        dtm.setRowCount(0);
        try{
            if(!id.isEmpty()){
                if(rs1.next()==true){
                    Vector<String> v = new Vector<>();
                    v.add(rs1.getString("id"));
                    v.add(rs1.getString("name"));
                    dtm.addRow(v);
                }else{
                    JOptionPane.showMessageDialog(this, "Invalid Id", "Search Route", JOptionPane.INFORMATION_MESSAGE);
                    jTextField2.setText("");
                    jTextField2.grabFocus();
                    loadTable();
                }
            }else{
                JOptionPane.showMessageDialog(this, "Please Enter Id", "Search Route", JOptionPane.INFORMATION_MESSAGE);
                jTextField2.setText("");
                jTextField2.grabFocus();
                loadTable();
            }
        }catch(Exception e){
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton7ActionPerformed


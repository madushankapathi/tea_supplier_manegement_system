    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String name = jTextField1.getText();
        ResultSet rs1 = DBMS.search("SELECT * FROM route WHERE `name`='"+name+"' ");
        try{
            if(!name.isEmpty()){
                if (rs1.next()==false) {
                    DBMS.iud("INSERT INTO route(`name`) VALUES('"+name+"')");
                    JOptionPane.showMessageDialog(this, "Route Add", "Success", JOptionPane.INFORMATION_MESSAGE);
                    loadTable();
                    jTextField1.setText("");
                    jTextField1.grabFocus();
                }else{
                    JOptionPane.showMessageDialog(this, "This Route Allredy Use", "Add Route", JOptionPane.INFORMATION_MESSAGE);
                    jTextField1.setText("");
                    jTextField1.grabFocus();
                }
            }else{
                JOptionPane.showMessageDialog(this, "Please Enter Route", "Add Route", JOptionPane.INFORMATION_MESSAGE);
                jTextField1.setText("");
                jTextField1.grabFocus();
            }
        }catch(Exception e){
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

